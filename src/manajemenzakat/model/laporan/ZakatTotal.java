public class ZakatTotal {
    private double zakatUang;
    private double zakatBeras;
    private double zakatMal;
    private double infaq;
    private double total;

    public double getZakatUang() {
        return zakatUang;
    }

    public void setZakatUang(double zakatUang) {
        this.zakatUang = zakatUang;
    }

    public double getZakatBeras() {
        return zakatBeras;
    }

    public void setZakatBeras(double zakatBeras) {
        this.zakatBeras = zakatBeras;
    }

    public double getZakatMal() {
        return zakatMal;
    }

    public void setZakatMal(double zakatMal) {
        this.zakatMal = zakatMal;
    }

    public double getInfaq() {
        return infaq;
    }

    public void setInfaq(double infaq) {
        this.infaq = infaq;
    }

    public double getTotal() {
        return total;
    }

    public void setTotal(double total) {
        this.total = total;
    }
    
    
}
